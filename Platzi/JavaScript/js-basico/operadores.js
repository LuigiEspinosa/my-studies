// ARITMETICOS

a + b   // Suma, afirmación unitaria, Positivo, concatenación
a - b   // Resta, negación unitaria, Negativo
a * b   // Producto
a / b   // Divisor
a % b   // Residuo
a ** b  // Potencia
a++     // Incremento (suma uno)
a--     // Decremento (resta uno)

// ASIGNACIÓN

a = b       // Asignación
a += b      // Asignación de adición
a -= b      // Asignación de sustracción
a *= b      // Asignación de multiplicación
a /= b      // Asignación de división
a %= b      // Asignación de residuo
a **= b     // Asignación de potencia
a <<= b     // Asignación de desplazamiento a la izquierda
a >>= b     // Asignación de desplazamiento a la derecha
a >>>= b    // Asignación sin signo de desplazamiento a la derecha
a &= b      // Asignación AND
a ^= b      // Asignación XOR
a |= b      // Asignación OR

// COMPARACIÓN

a == b      // Igualdad
a != b      // Diferencia
a === b     // Identidad
a !== b     // Sin Identidad
a > b       // Mayor que
a >= b      // Mayor o igual que
a < b       // Menor que
a <= b      // Menor o igual que

// LOGICOS

a && b      // AND, Y
a || b      // OR, O
!a          // NOT, Negación

// LOGICOS A NIVEL DE BITS

a << b      // Desplazamiento a la izquierda
a >> b      // Desplazamiento a la derecha
a >>> b     // Desplazamiento a la derecha sin signo
a & b       // AND
a ^ b       // XOR
a | b       // OR
~a          // NOT